import Foundation

struct Calculations {
    func sum(n1:Int, n2:Int) -> (Int) {
        return n1 + n2
    }
    
    func sub(n1:Int, n2:Int) -> (Int) {
        return n1 - n2
    }
    
    func multi(n1:Int, n2:Int) -> (Int) {
        return n1 * n2
    }
    
    func div(n1:Int, n2:Int) -> (Int) {
        return n1 / n2
    }
}
